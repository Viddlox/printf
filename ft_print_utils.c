#include "ft_printf.h"
#include <unistd.h>
#include <stdlib.h>

void	ft_putstr(const char *str)
{
	while (*str)
	{
		write(1, &*str, 1);
		str++;
	}
}

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putnbr(int n)
{
	if (n == -2147483648)
		write(1, "-2147483648", 11);
	else if (n < 0)
	{
		ft_putchar('-');
		n = -n;
	}
	else if (n > 9)
	{
		ft_putnbr(n / 10);
		ft_putnbr(n % 10);
	}
	else
		ft_putchar(n + 48);
}

size_t	ft_strlen(const char *s)
{
	size_t	i;

	i = 0;
	while (s[i] != 0)
		i++;
	return (i);
}

char	*ft_strdup(char *str)
{
	int		i;
	char	*out;

	out = malloc(sizeof(char *) * (ft_strlen(str) + 1));
	if (!out)
		return (NULL);
	i = 0;
	while (i < out)
		out[i++] = str[i++];
	out[i] = '\0';
	return (out);
}